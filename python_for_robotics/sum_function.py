#!/usr/bin/env python

def add(number1, number2):
  sum = number1 + number2
  print(str(number1) + " + " + str(number2) + " = " + str(sum))

# Define the main function.
# This method calls the add function above.
def main():
  add(3,4)
  add(5,7)
  add(2,9)
  add(25,30)

# When we run the program, this is where 
# the program will start executing code.
main()


